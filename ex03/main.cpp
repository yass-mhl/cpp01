#include "HumanA.hpp"
#include "HumanB.hpp"

int main()
{
	// Human A doit obligatoirement avoir une arme
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}

	// Human B n'a pas obligatoirement d'arme
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}
